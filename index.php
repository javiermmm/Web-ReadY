		<h2 class="title">El Microrrelato</h2>
		<div class="entry">
				<p>HISTORIA DEL GÉNERO</p>

				<p>Textos escritos u orales de corta extensión aparecen a lo largo de todos los tiempos. Fábulas, adivinanzas, parábolas, epitafios, graffittis, etcétera. El microrrelato no es un fenómeno nuevo, sienta sus bases en la Edad Media en los llamados bestiarios y más adelante es posible encontrarlo en las sentencias del Conde Lucanor, pero aún más atrás existen antecedentes en las parábolas de Jesús, vistas de forma individual, separada del texto, como estructuras narrativas completas y breves, exigencia de un microcuento.
				En la Argentina, en 1955 Jorge Luis Borges junto con Adolfo Bioy Casares publicaron Cuentos breves y extraordinarios, que contiene relatos de entre dos páginas y dos líneas. Bioy Casares publicó Guirnalda con amores en 1959 y al año siguiente Borges publicó El Hacedor, que incluye varios microrrelatos. Julio Cortázar masificó el género con Historias de Cronopios y de Famas haciéndolo famoso en Europa.
				En España hay referentes clásicos en este género, desde Ramón Gómez de la Serna, Juan Ramón Jiménez y Max Aub, hasta Albert Tugues y otros tantos que en la actualidad cultivan el microcuento con gran prolijidad.
				En Estados Unidos se empieza en los años 70 y fuera del Río de la Plata en Latinoamérica se comienza a explorar con mayor intensidad a partir de la década del 80. Es, sin duda, reflejo de lo absurdo y lo fragmentario de la modernidad. Juan José Arreola, Marco Denevi y Augusto Monterroso crearon las condiciones para que el microrrelato irrumpiera con mayor fuerza.
				En la literatura francesa, los autores de microficciónes (micronouvelles), como Jacques Fuentealba, Vincent Bastin y Stéphane Bataillon, son fuertemente influidos por la microficción española.
				En alemán, las Kürzestgeschichten, especialmente influidas por las narraciones breves de Bertolt Brecht y Franz Kafka, conocen autores como Peter Bichsel, Heimito von Doderer, Helmut Heißenbüttel y Günter Kunert.</p>
				
				<p>Dice Pedro de Miguel, en el diario El Mundo de Madrid:
				Pero es en la época moderna, al nacer el cuento como género literario, cuando el microrrelato se populariza en la literatura en español gracias a la concurrencia de dos fenómenos de distinta índole: la explosión de las vanguardias con su renovación expresiva y la proliferación de revistas que exigían textos breves ilustrados para llenar sus páginas culturales. Algunas de las greguerías de Ramón Gómez de la Serna son verdaderos cuentos de apenas una línea, y también Rubén Darío y Vicente Huidobro publicaron minicuentos desde diversas estéticas. Junto a estos autores, la crítica señala también al mexicano Julio Torri y al argentino Leopoldo Lugones como decisivos precursores del actual microrrelato.
				En la segunda mitad del siglo XX, proliferan estos textos que David Lagmanovich llama "cuentos concentrados al máximo, bellos como teoremas [...] que ponen a prueba nuestras maneras rutinarias de leer". Se presenta como una auténtica propuesta literaria, al grado de que es raro el escritor que no lo haya intentado.</p>
				
				<p>Por otro lado, Víctor Montoya afirma que:
				Los microrrelatos son una apuesta por la literatura futurista cuyas innovadoras técnicas responden a las exigencias de un mundo más moderno, donde el tiempo es plata y la prosa breve es oro [...] Los narradores que valoran lo efímero en la literatura, cultivan una prosa breve, mientras más breve mejor. Se trata de una literatura que está muy cerca de prosa poética y que, al mejor estilo de los haikus, se parece a un félido veloz y cimbreante, constituido más por músculos que por grasa.</p>
				
				<p>No es de extrañar entonces que los mecanismos de construcción del microcuento contemplen el quiebre de expectativas del lector que lo impele a seguir buscando el sentido, y no sólo esto, sino además otorgar el sentido al completar los significados que apenas se esbozan o se insinúan apoyados en otro de los mecanismos propios de su construcción, "el doble sentido", es decir, la posibilidad de que lo dicho signifique no sólo lo literal e incluso algunas veces en absoluto lo literal, sino otra cosa que el lector debe encontrar para completar la narración, siendo de este modo lector y autor al mismo tiempo.</p>
				
				<p>Juan Armando Epple ha definido así este tipo de composiciones:</p>
				
				<p>Lo que distingue a estos textos como relatos es la existencia de una situación narrativa única, formulada en un espacio imaginario y en su decurso temporal, aunque algunos elementos de esta tríada (acción, espacio, tiempo), estén simplemente sugeridos.
				Para entender el microcuento no se necesitan referentes externos salvo aquellos de orden cultural que permiten otorgar sentido a algunas expresiones. Esta narración es por sobre todo concisa, es decir, breve y precisa y de una gran intensidad expresiva, pues es un verdadero extracto no en el sentido del resumen, sino en el sentido de "esencia", es decir, aquello que contiene lo más importante, lo central.</p>
				
