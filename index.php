			<p>PRINCIPALES CARACTERÍSTICAS</p>


				<p>La brevedad</p>
				<p>No es falso asegurar que la brevedad, noción que también aplica al cuento, sea lo más común de este tipo de textos. Sin embargo, es una característica bastante subjetiva, ya que existen microrrelatos de más de una página. Contar el número de palabras es sólo una forma de ilustrar el concepto de brevedad.
				Los títulos suelen ser pertinentes, porque pueden ayudar a la focalización o a completar aquello que, por la brevedad, no se dice. En algunos casos son imprescindibles para completar el sentido.</p>
				
				<p>La temática</p>
				<p>Como otras obras literarias, los microcuentos abarcan las más diversas temáticas que van desde la ficción pura, la inclusión de otros discursos (políticos, sociales, etcétera), hasta el uso de la intertextualidad. Tiene la particularidad de presentar un desenlace inesperado con respecto a los sucesos narrados, es decir, quiebra las expectativas del lector sobre su final, condicionado por los sucesos narrados antes del descenlace.</p>
				
				<p>Cruce de géneros</p>
				<p>La economía de palabras es notoria, pero a medida que el microrrelato se va haciendo popular, también la variedad de la forma va aumentando. Esto provoca la destrucción de los géneros, hasta el punto de que resulte imposible -e inútil- tratar de definirlo, distinguirlo o clasificarlo. Se postula entonces un género híbrido que con sus recursos estilísticos entremezcla narración, ensayo, poesía, entre otros.</p>
